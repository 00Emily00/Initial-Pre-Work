// The pop() function is the opposite of the push() function. Instead of adding things to the array, you are taking away (popping off) that specified data.

// Example
var ourArray = [1,2,3];
var removedFromOurArray = ourArray.pop(); 
// removedFromOurArray now equals 3, and ourArray now equals [1,2]

