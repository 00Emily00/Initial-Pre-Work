// Parameters are what you put inside the paranthesis after the function name and before the curly braces. They are variables that act as placeholders for the values that will be inputted when function is called. The values that are passed (inputted) into a function is known as an argument

// Example
function ourFunctionWithArgs(a, b) {
  console.log(a - b);
}
ourFunctionWithArgs(10, 5); // Outputs 5

// Only change code below this line.
function functionWithArgs(arg1, arg2) {
  console.log(arg1 + arg2);
}
